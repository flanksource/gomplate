package gencel

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/types"
	"log"
	"text/template"

	"golang.org/x/tools/go/packages"
)

type Package struct {
	name  string
	defs  map[*ast.Ident]types.Object
	files []*File
}

type Generator struct {
	pkg *Package
	buf bytes.Buffer // Accumulated output.
}

func (t *Generator) Execute(patterns ...string) {
	cfg := &packages.Config{
		Mode:  packages.LoadSyntax,
		Tests: false,
	}
	pkgs, err := packages.Load(cfg, patterns...)
	if err != nil {
		log.Fatal(err)
	}
	if len(pkgs) != 1 {
		log.Fatalf("error: %d packages found", len(pkgs))
	}

	t.addPackage(pkgs[0])
}

func (g *Generator) addPackage(pkg *packages.Package) {
	g.pkg = &Package{
		name:  pkg.Name,
		defs:  pkg.TypesInfo.Defs,
		files: make([]*File, len(pkg.Syntax)),
	}

	for i, file := range pkg.Syntax {
		g.pkg.files[i] = &File{
			file: file,
			pkg:  g.pkg,
		}
	}
}

func (g *Generator) Generate() {
	// Print the header and package clause.
	g.Printf("// Code generated by \"gencel\";\n// DO NOT EDIT.\n")
	g.Printf("\n")
	g.Printf("package %s", g.pkg.name)
	g.Printf("\n")
	g.Printf("import %s\n", `"github.com/google/cel-go/common/types"`)
	g.Printf("import %s\n", `"time"`)
	g.Printf("import %s\n", `"github.com/google/cel-go/cel"`)
	g.Printf("import %s\n", `"github.com/google/cel-go/common/types/ref"`)
	g.Printf("\n")

	for _, file := range g.pkg.files {
		ast.Inspect(file.file, file.genDecl)

		for _, decl := range file.decls {
			v := funcDefTemplateView{
				FnName:     decl.Name,
				Args:       getCelArgs(decl.Args),
				ReturnType: astToIdent(decl.ReturnType),
				RecvType:   decl.RecvType,
			}
			g.render(funcDefTemplate, v)
		}
	}
}

func (g *Generator) Printf(format string, args ...interface{}) {
	_, err := fmt.Fprintf(&g.buf, format, args...)
	if err != nil {
		log.Fatal(err)
	}
}

func (g *Generator) render(tpl string, model interface{}) {
	t, err := template.New(tpl).Funcs(tplFuncs).Parse(tpl)
	if err != nil {
		log.Fatal("instance template parse: ", err)
	}

	err = t.Execute(&g.buf, model)
	if err != nil {
		log.Fatal("Execute: ", err)
		return
	}
}

func (g *Generator) Format() []byte {
	src, err := format.Source(g.buf.Bytes())
	if err != nil {
		log.Printf("warning: internal error: invalid Go generated: %s", err)
		log.Printf("warning: compile the package to analyze the error")
		return g.buf.Bytes()
	}
	return src
}
